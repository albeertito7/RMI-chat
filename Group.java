public class Group {
	
	private String name;

	public Group (String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}
}

/*
* Class representing the Groupo entity
* It is the model

* It is currently a very basic class, although it is ready to extend its functionality,
* either by adding for example a group administrator ... etc.

* So this is where we define the aspects regarding the groups, i.e. what they should be like.
*/